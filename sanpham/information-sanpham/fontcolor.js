function fontcolorbefore(x)
{

	x.style.backgroundColor="#ffffff";
	x.style.backgroundSize="100%";

}
function fontcolorafter(x)
{
	x.style.backgroundColor="#F8F8FF";
	x.style.backgroundSize="100%";
}



